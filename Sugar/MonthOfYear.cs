namespace System
{
    /// <summary>
    /// Exposes months for a <see cref="System.DateTime"/> as a <see cref="System.Enum"/>.
    /// </summary>
    public enum MonthOfYear
    {
        /// <summary>
        /// Represents the first month of the year.
        /// Supplies an underlying value of 1.
        /// </summary>
        January = 1,

        /// <summary>
        /// Represents the second month of the year.
        /// Supplies an underlying value of 2.
        /// </summary>
        February,

        /// <summary>
        /// Represents the third month of the year.
        /// Supplies an underlying value of 3.
        /// </summary>
        March,

        /// <summary>
        /// Represents the fourth month of the year.
        /// Supplies an underlying value of 4.
        /// </summary>
        April,

        /// <summary>
        /// Represents the fifth month of the year.
        /// Supplies an underlying value of 5.
        /// </summary>
        May,

        /// <summary>
        /// Represents the sixth month of the year.
        /// Supplies an underlying value of 6.
        /// </summary>
        June,

        /// <summary>
        /// Represents the seventh month of the year.
        /// Supplies an underlying value of 7.
        /// </summary>
        July,

        /// <summary>
        /// Represents the eighth month of the year.
        /// Supplies an underlying value of 8.
        /// </summary>
        August,

        /// <summary>
        /// Represents the nineth month of the year.
        /// Supplies an underlying value of 9.
        /// </summary>
        September,

        /// <summary>
        /// Represents the tenth month of the year.
        /// Supplies an underlying value of 10.
        /// </summary>
        October,

        /// <summary>
        /// Represents the eleventh month of the year.
        /// Supplies an underlying value of 11.
        /// </summary>
        November,

        /// <summary>
        /// Represents the twelveth month of the year.
        /// Supplies an underlying value of 12.
        /// </summary>
        December
    }
}